"""Load the locally-built StealthMole dataset into a masked threat landscape.

`build_dataset` writes masked records under `data/stealthmole/dataset/<run>/`
(git-ignored). This loader reads the latest run and computes demo-safe
aggregates ("landscape") that the readiness simulator surfaces in the mission
desk ThreatIntel view. All inputs are already masked; this only aggregates.

If no collected dataset is present (e.g. a fresh checkout — collected data is
never committed), the loader falls back to the fully synthetic demo run under
`data/stealthmole/demo/` (tracked in git, generated by `d4d.build_demo_dataset`)
so the mission-desk ThreatIntel view still renders a landscape. If neither
exists, `load_threat_landscape()` returns None and the simulator uses its
inline synthetic fixture.
"""

from __future__ import annotations

import json
from collections import Counter
from pathlib import Path
from typing import Any

from ..config import project_root

_RECENT = 5
_TOP_N = 6
_cache: dict[str, Any] | None = None
_loaded = False


def _dataset_root() -> Path:
    return project_root() / "data" / "stealthmole" / "dataset"


def _demo_root() -> Path:
    return project_root() / "data" / "stealthmole" / "demo"


def _latest_run_in(root: Path) -> Path | None:
    if not root.is_dir():
        return None
    runs = sorted((p for p in root.iterdir() if p.is_dir()), reverse=True)
    return runs[0] if runs else None


def _latest_run_dir() -> Path | None:
    # 실수집 데이터셋(로컬 전용) 우선, 없으면 커밋된 synthetic demo 런으로 폴백.
    return _latest_run_in(_dataset_root()) or _latest_run_in(_demo_root())


def _read_feed(run_dir: Path, service: str) -> dict[str, Any]:
    path = run_dir / f"{service}.json"
    if not path.is_file():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (ValueError, OSError):
        return {}


def _top(counter: Counter, n: int = _TOP_N) -> dict[str, int]:
    return {k: v for k, v in counter.most_common(n)}


def _build_landscape(run_dir: Path) -> dict[str, Any]:
    rm = _read_feed(run_dir, "rm")
    gm = _read_feed(run_dir, "gm")
    lm = _read_feed(run_dir, "lm")
    cl = _read_feed(run_dir, "cl")
    cds = _read_feed(run_dir, "cds")

    rm_rows = rm.get("records") or []
    cred_rows = (cl.get("records") or []) + (cds.get("records") or [])
    gm_rows = gm.get("records") or []
    lm_rows = lm.get("records") or []

    groups = Counter(r.get("attack_group") for r in rm_rows if r.get("attack_group"))
    sectors = Counter(r.get("sector") for r in rm_rows if r.get("sector"))
    countries = Counter(r.get("country") for r in rm_rows if r.get("country"))

    domains = Counter(r.get("domain") for r in cred_rows if r.get("domain"))
    stealers = Counter(r.get("stealer") for r in cred_rows if r.get("stealer"))
    with_password = sum(1 for r in cred_rows if r.get("password"))

    def feed_total(feed: dict[str, Any]) -> int:
        return int((feed.get("stats") or {}).get("totalCount") or 0)

    synthetic_demo = run_dir.parent.name == "demo"
    return {
        "source": "synthetic-demo" if synthetic_demo else "stealthmole:masked",
        "synthetic": synthetic_demo,
        "dataset_run": run_dir.name,
        "sampled_records": len(rm_rows) + len(cred_rows) + len(gm_rows) + len(lm_rows),
        "ransomware": {
            "sampled": len(rm_rows),
            "feed_total": feed_total(rm),
            "top_groups": _top(groups),
            "top_sectors": _top(sectors, 5),
            "top_countries": _top(countries, 5),
            "recent": [
                {
                    "victim": r.get("victim"),
                    "attack_group": r.get("attack_group"),
                    "sector": r.get("sector"),
                    "detected_at": r.get("detected_at"),
                }
                for r in rm_rows[:_RECENT]
            ],
        },
        "credentials": {
            "sampled": len(cred_rows),
            "feed_total": feed_total(cl) + feed_total(cds),
            "with_password": with_password,
            "top_domains": _top(domains),
            "stealers": _top(stealers, 5),
            "recent_samples": [
                {
                    "email": r.get("email"),
                    "domain": r.get("domain"),
                    "password": r.get("password"),
                    "leaked_date": r.get("leaked_date"),
                }
                for r in cred_rows[:_RECENT]
            ],
        },
        "monitoring": {
            "government_sampled": len(gm_rows),
            "leaked_sampled": len(lm_rows),
            "recent_titles": [
                {"title": r.get("title"), "author": r.get("author"), "detected_at": r.get("detected_at")}
                for r in (gm_rows + lm_rows)[:_RECENT]
            ],
        },
    }


def load_threat_landscape(*, refresh: bool = False) -> dict[str, Any] | None:
    """Return the masked StealthMole landscape, or None if no dataset exists."""
    global _cache, _loaded
    if _loaded and not refresh:
        return _cache
    run_dir = _latest_run_dir()
    _cache = _build_landscape(run_dir) if run_dir else None
    _loaded = True
    return _cache
